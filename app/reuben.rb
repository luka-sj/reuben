#===============================================================================
#  Application handling module
#===============================================================================
module Reuben
  #-----------------------------------------------------------------------------
  class << self
    #---------------------------------------------------------------------------
    #  start application logic
    #---------------------------------------------------------------------------
    def start
      log "Starting Reuben v#{Env::REUBEN_VERSION}"
      Database::Connector.connect
      Discord.initialize
      Discord.route
      Discord.start
    rescue
      Env.error('Failed to start Reuben application!')
    end
    #---------------------------------------------------------------------------
  end
  #-----------------------------------------------------------------------------
end
